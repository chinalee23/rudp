package main

import (
	"container/list"
	"log"
	"rudp"
)

var index int

func dump(queue *list.List) {
	for e := queue.Front(); e != nil; e = e.Next() {
		data := e.Value.([]byte)
		log.Println("pkg[", index, "]:", data)
		index++
	}
}

